public sealed class FakeCodeAgent(IRunLogger logger) : IAgent
{
    public string Name => "CodeAgent";

    public async Task<AgentResult> ExecuteAsync(AgentContext context, CancellationToken cancellationToken)
    {
        await logger.InfoAsync($"{Name} started", cancellationToken);
        var srcDirectory = Path.Combine(context.Run.Paths.WorkspaceDirectory, "src");
        Directory.CreateDirectory(srcDirectory);
        await File.WriteAllTextAsync(Path.Combine(context.Run.Paths.WorkspaceDirectory, "README.md"), "# Workspace\n\nGenerated by the fake code agent.\n", Encoding.UTF8, cancellationToken);
        await File.WriteAllTextAsync(Path.Combine(srcDirectory, "placeholder.txt"), "Placeholder source file generated by the fake code agent.\n", Encoding.UTF8, cancellationToken);
        await logger.InfoAsync($"{Name} completed", cancellationToken);
        return AgentResult.Success(
            "Workspace placeholders generated.",
            [
                new ArtifactRef(ArtifactType.Code, "workspace/README.md", "Workspace README"),
                new ArtifactRef(ArtifactType.Code, "workspace/src/placeholder.txt", "Placeholder source")
            ]);
    }
}

