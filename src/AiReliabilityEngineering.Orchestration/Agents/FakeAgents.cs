public sealed class FakeRequirementsAgent(IRunLogger logger) : IAgent
{
    public string Name => "RequirementsAgent";

    public async Task<AgentResult> ExecuteAsync(AgentContext context, CancellationToken cancellationToken)
    {
        await logger.InfoAsync($"{Name} started", cancellationToken);
        var relativePath = "artifacts/specification.json";
        var path = Path.Combine(context.Run.Paths.ArtifactsDirectory, "specification.json");
        await File.WriteAllTextAsync(path, """
            {
              "name": "placeholder-project",
              "summary": "Generated by fake requirements agent.",
              "requirements": [
                "Read initial idea file",
                "Generate placeholder workflow artifacts"
              ]
            }
            """, Encoding.UTF8, cancellationToken);
        await logger.InfoAsync($"{Name} completed", cancellationToken);
        return AgentResult.Success("Requirements placeholder generated.", [new ArtifactRef(ArtifactType.Specification, relativePath, "Placeholder specification")]);
    }
}

